#  Write a program that reads a Celsius degree from the console and converts it to Fahrenheit
#  and displays the result. The formula for the conversion is as follows: fahrenheit = (9 / 5) * celsius + 32

celsius = eval(input("Enter a degree in celsius : "))
fahrenheit = (9 / 5) * celsius + 32
print(celsius, "celsius is equal to ", fahrenheit, "fahrenheit")



